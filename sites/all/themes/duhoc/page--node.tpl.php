		</tr></tbody></table><p class="Normal">
	"Người này đã thừa nhận có hành động thù địch (với Triều Tiên) khi tiến vào lãnh thổ Triều Tiên dưới vỏ bọc một khách du lịch hồi tháng 4", hãng thông tấn nhà nước Triều Tiên <em>KCNA</em> cho biết trong một thông báo. Miller, đến từ bang California, bị bắt hồi tháng 4, sau khi Bình Nhưỡng buộc tội ông tự xé rách visa và đưa ra yêu cầu tị nạn.</p>
<p class="Normal">
	Ngoài Miller, Triều Tiên còn đang giữ hai công dân Mỹ khác là Jeffrey Fowle và Kenneth Bae. Ba công dân Mỹ hôm 1/9 từng kêu gọi sự giúp đỡ từ chính phủ Mỹ trong một cuộc phỏng vấn với <em>CNN</em>. Họ muốn Washington cử một phái đoàn tới Triều Tiên để đàm phán.</p>
<p class="Normal">
	"Tình trạng của tôi là rất khẩn cấp", Miller nói. "Tôi sẽ sớm bị xét xử và bỏ tù. Tôi nghĩ đây là cơ hội cuối cùng để kêu gọi chính phủ Mỹ giúp đỡ". Trong khi đó, Uri Tours, đơn vị có trụ sở tại Mỹ, thường đưa khách du lịch tới Triều Tiên, cho biết họ không hiểu tại sao Miller lại tự xé visa.</p>
<p class="Normal">
	Fowle, đến từ bang Ohio, bị bắt tại Bình Nhưỡng hồi tháng 5 với cáo buộc bỏ lại một cuốn kinh thánh tại khách sạn. Hiện Triều Tiên chưa công bố ngày xét xử người đàn ông 56 tuổi này. Trong khi đó, Bae là một nhà truyền giáo người Mỹ gốc Hàn đang phải thực thi bản án 15 năm lao động khổ sai tại một trại giam bên ngoài thủ đô Bình Nhưỡng vì tội "âm mưu lật đổ chính phủ Triều Tiên".</p>
<p class="Normal">
	Trước đó, Mỹ từng nhiều lần lên tiếng yêu cầu Triều Tiên ân xá và thả công dân của nước này nhưng không được đáp lại. Triều Tiên cũng thường dùng những người Mỹ bị bắt để làm con bài mặc cả ngoại giao.</p>
